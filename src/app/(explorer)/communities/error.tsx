"use client";

import { useEffect } from "react";

export default function Error({
  error,
  reset,
}: {
  error: Error;
  reset: () => void;
}) {
  useEffect(() => {
    // Log the error to an error reporting service
    console.error(error);
  }, [error]);

  return (
    <div>
      <p>Error: Oh no! Something went wrong.</p>
      <button onClick={() => reset()}>Try Again</button>
    </div>
  );
}
